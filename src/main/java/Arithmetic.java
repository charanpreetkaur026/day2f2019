public class Arithmetic
{
 int add(int a, int b)
 {
     int sum;
     sum = a + b;
     return sum;
 }
 int sub(int a, int b)
 {
     int c;
     c = a - b;
     return c;
 }
 int mul(int a, int b)
 {
     int c;
     c = a * b;
     return c;
 }
 float div(int a, int b)
 {
     float c;
     c = (float) (a / b);
     return c;
 }

}
